package com.myproject.model;

public class Friend {
	
	private int fid;
	private String fname;
	private int fsex;
	
	public int getFid() {
		return fid;
	}
	public void setFid(int fid) {
		this.fid = fid;
	}
	public String getFname() {
		return fname;
	}
	public void setFname(String fname) {
		this.fname = fname;
	}
	public int getFsex() {
		return fsex;
	}
	public void setFsex(int fsex) {
		this.fsex = fsex;
	}
}
